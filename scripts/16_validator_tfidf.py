import pandas as pd
import joblib
import re

# Cleaning function (must be identical to the one used in training)
def clean_java_code(code):
    if not isinstance(code, str): return ""
    code = re.sub(r'//.*', '', code)
    code = re.sub(r'/\*.*?\*/', '', code, flags=re.DOTALL)
    code = re.sub(r'\s+', ' ', code)
    return code.strip().lower()

def main():
    print("⚖️ Loading the Judge (Random Forest Model)...")
    model = joblib.load('./trained_models/resource_leak_validator.pkl')
    vectorizer = joblib.load('./trained_models/vectorizer.pkl')

    # Load tournament results
    df = pd.read_csv('./data_bases/05_results/llm_tournament_results.csv') # or the name of your spreadsheet
    
    llm_models = ['fix_gpt', 'fix_claude', 'fix_gemini']
    victories = {model: 0 for model in llm_models}

    print("\n🧐 Analyzing the fixes...")

    for llm in llm_models:
        # 1. Clean the code generated by the LLM
        cleaned_codes = df[llm].apply(clean_java_code)
        
        # 2. Vectorize
        X_vectorized = vectorizer.transform(cleaned_codes)
        
        # 3. Ask the Judge for a verdict (0 = Safe, 1 = With Bug)
        predictions = model.predict(X_vectorized)
        
        # Save the result in the spreadsheet for manual review later
        df[f'verdict_{llm}'] = predictions
        
        # Count how many codes the Judge considered "Safe" (Class 0)
        safe_cases = (predictions == 0).sum()
        victories[llm] = safe_cases

    # Display Final Score
    print("\n" + "="*30)
    print("🏆 FINAL TOURNAMENT SCORE")
    print("="*30)
    total = len(df)
    for llm, wins in victories.items():
        rate = (wins / total) * 100
        print(f"{llm.upper()}: {wins}/{total} bugs fixed ({rate:.2f}%)")
    print("="*30)

    # Save final spreadsheet with verdicts
    df.to_csv('./data_bases/05_results/final_dissertation_report.csv', index=False, sep=';')
    print("\n✅ Detailed report saved to 'final_dissertation_report.csv'")

if __name__ == "__main__":
    main()